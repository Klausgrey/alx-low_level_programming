#include <stdio.h>

/**
* add - adds two numbers
* @a: paramter
* @b: parameter
*/

int add(int a , int b)
{
	return (a + b);
}

/**
* sub - subtracts two numbers
* @a: paramter
* @b: parameter
*/

int sub(int a, int b)
{
	return (a - b);
}

/**
* mul - multiplies two numbers
* @a: paramter
* @b: parameter
*/

int mul(int a, int b)
{
	return (a * b);
}

/**
* mod - gets the remnainder two numbers
* @a: paramter
* @b: parameter
*/

int mod(int a, int b)
{
	return (a % b);
}